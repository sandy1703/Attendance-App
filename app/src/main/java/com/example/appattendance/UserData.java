package com.example.appattendance;

public class UserData {

    public String Name;
    public String Email;
    public String Class;
    public String Contact;
    public String UserId;


    public UserData() {
    }

    public UserData(String name, String email, String aClass, String contact, String userId) {
        Name = name;
        Email = email;
        Class = aClass;
        Contact = contact;
        UserId = userId;
    }
}
